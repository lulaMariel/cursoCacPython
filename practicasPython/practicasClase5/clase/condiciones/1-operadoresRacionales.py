# "==" : Igual que

print("\n")
print(10 == 10) # True
print(10 == 15) # False
print("\n")

# "!=" : Diferente que
print(10 != 10) # False
print(10 != 15) # True
print("\n")

# ">" : Mayor que
print(10 > 15) # False
print(15 > 10) # True
print("\n")

# "<" : Menor que
print(10 < 15) # True
print(15 < 10) # False
print("\n")

# >= : Mayor o igual
print(10 >= 9) # True
print(10 >= 10) # True
print(10 >= 15) # False
print("\n")

# <= : Menor o igual
print(10 <= 9) # False
print(10 <= 10) # True
print(10 <= 15) # True
print("\n")